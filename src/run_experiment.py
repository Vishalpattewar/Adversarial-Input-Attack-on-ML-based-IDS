# ─────────────────────────────────────────────────────────────
#  STEP 1 — LOAD DATASET
# ─────────────────────────────────────────────────────────────
def load_dataset() -> pd.DataFrame:
    """
    Load network_flows_cicids.csv and validate contents.

    WHY HARD STOP:
      No synthetic data fallback — forces real data
      Training on fake data = meaningless results
      If CSV missing: run prepare_cicids.py first

    Returns:
        df: DataFrame with 52 numeric features + 'label' column
            100,000 rows, binary labels (0=Normal, 1=Attack)

    Raises:
        SystemExit: if CSV file not found
        SystemExit: if required columns missing
        SystemExit: if label column has wrong values
    """
    print(f'{"=" * 60}')
    print(f'  STEP 1 — Loading CICIDS2017 Dataset')
    print(f'{"=" * 60}')

    # ── Hard stop if CSV missing ──────────────────────────────
    # WHY: training must use real data — no synthetic fallback
    if not os.path.exists(CSV_PATH):
        print(f'\n  ✗ ERROR: Dataset not found:')
        print(f'    {CSV_PATH}')
        print(f'\n  Run prepare_cicids.py first:')
        print(f'    cd ~/A-IDS/adversarial_ids_cicids')
        print(f'    python3 prepare_cicids.py')
        print()
        sys.exit(1)

    # ── Load CSV ──────────────────────────────────────────────
    print(f'  Loading: {CSV_PATH}')
    t_start = time.time()
    df      = pd.read_csv(CSV_PATH)
    t_load  = time.time() - t_start
    print(f'  Loaded in {t_load:.1f}s')

    # ── Validate required feature columns exist ───────────────
    # WHY: if prepare_cicids.py used different features →
    #      model would train on wrong data silently
    missing_cols = [
        f for f in NUMERIC_FEATURES if f not in df.columns
    ]
    if missing_cols:
        print(f'\n  ✗ ERROR: Missing feature columns:')
        for col in missing_cols:
            print(f'    → {col}')
        print(f'\n  Re-run prepare_cicids.py to regenerate CSV')
        sys.exit(1)

    # ── Validate label column exists ──────────────────────────
    if 'label' not in df.columns:
        print(f'\n  ✗ ERROR: "label" column not found in CSV')
        print(f'  Re-run prepare_cicids.py to regenerate CSV')
        sys.exit(1)

    # ── Validate binary labels ────────────────────────────────
    label_vals = set(df['label'].unique())
    if not label_vals.issubset({0, 1}):
        print(f'\n  ✗ ERROR: Labels are not binary: {label_vals}')
        print(f'  Re-run prepare_cicids.py to regenerate CSV')
        sys.exit(1)

    # ── Print dataset summary ─────────────────────────────────
    n_total  = len(df)
    n_normal = (df['label'] == 0).sum()
    n_attack = (df['label'] == 1).sum()

    print(f'\n  Dataset Summary:')
    print(f'  {"─" * 40}')
    print(f'  Total samples  : {n_total:,}')
    print(f'  Normal traffic : {n_normal:,} '
          f'({n_normal / n_total * 100:.1f}%)')
    print(f'  Attack traffic : {n_attack:,} '
          f'({n_attack / n_total * 100:.1f}%)')
    print(f'  Features       : {len(NUMERIC_FEATURES)}')
    print(f'  Input dim      : {len(NUMERIC_FEATURES)} '
          f'(for IDSNet)')
    print(f'  Source         : CICIDS2017 '
          f'(network_flows_cicids.csv)')
    print(f'  {"─" * 40}')

    return df


# ─────────────────────────────────────────────────────────────
#  STEP 2 + 3 — TRAIN MODELS
# ─────────────────────────────────────────────────────────────
def train_model(df         : pd.DataFrame,
                adversarial: bool) -> IDSTrainer:
    """
    Train one IDS model (standard or adversarial).

    Standard   mode: trains on clean data only
    Adversarial mode: trains on clean + FGSM-perturbed data
                      uses fgsm_epsilon=0.80

    Args:
        df         : Full DataFrame from load_dataset()
        adversarial: True = adversarial training with FGSM

    Returns:
        trainer: Fitted IDSTrainer with saved model files
                 Ready for attack evaluation
    """
    mode = 'ADVERSARIAL' if adversarial else 'STANDARD'

    print(f'\n{"=" * 60}')
    print(f'  STEP {"3" if adversarial else "2"} — '
          f'Training {mode} IDS Model')
    print(f'{"=" * 60}')

    # Create trainer — model_dir auto-set to models/cicids/
    # via __file__ anchored _DEFAULT_MODEL_DIR in ids_model.py
    trainer = IDSTrainer(model_dir=MODELS_DIR)

    # Train model
    # Returns metrics dict with accuracy, type, input_dim
    t_start = time.time()

    metrics = trainer.train(
        df           = df,
        epochs       = EPOCHS,
        batch_size   = BATCH_SIZE,
        lr           = LEARNING_RATE,
        adversarial  = adversarial,
        fgsm_epsilon = FGSM_EPSILON
    )

    t_train = time.time() - t_start

    print(f'\n  Training complete in {t_train / 60:.1f} minutes')
    print(f'  Accuracy : {metrics["accuracy"] * 100:.2f}%')
    print(f'  Type     : {metrics["type"]}')
    print(f'  input_dim: {metrics["input_dim"]}')

    return trainer


# ─────────────────────────────────────────────────────────────
#  STEP 4 + 5 — RUN ATTACKS
# ─────────────────────────────────────────────────────────────
def run_attacks(trainer    : IDSTrainer,
                df         : pd.DataFrame,
                model_type : str) -> dict:
    """
    Run FGSM and PGD epsilon sweeps against one trained model.

    Extracts ATTACK_SAMPLES attack-class samples.
    Runs FGSM sweep across all EPSILONS.
    Runs PGD sweep across all EPSILONS.
    Saves JSON results to results/cicids/{model_type}/

    Args:
        trainer   : Fitted IDSTrainer (model + scaler loaded)
        df        : Full DataFrame (to extract attack samples)
        model_type: 'standard' or 'adversarial'
                    Used for output subdirectory name

    Returns:
        dict with keys:
          'fgsm': epsilon_sweep results for FGSM
          'pgd' : epsilon_sweep results for PGD
    """
    step_num = '4' if model_type == 'standard' else '5'

    print(f'\n{"=" * 60}')
    print(f'  STEP {step_num} — Attacking {model_type.upper()} Model')
    print(f'{"=" * 60}')

    # ── Extract attack samples ────────────────────────────────
    # Use only label=1 (attack) samples for evasion evaluation
    # WHY: we want to measure how many attacks EVADE detection
    #      not how many normal samples get misclassified
    attack_df = df[df['label'] == 1]

    # Sample ATTACK_SAMPLES rows from attack class
    # random_state=42 ensures reproducibility
    n_sample = min(ATTACK_SAMPLES, len(attack_df))
    sample   = attack_df.sample(n=n_sample, random_state=42)

    X_attack = sample[NUMERIC_FEATURES].values.astype(np.float32)
    y_attack = sample['label'].values.astype(np.float32)

    print(f'\n  Attack samples : {len(X_attack):,}')
    print(f'  All labels = 1 : '
          f'{(y_attack == 1).all()} ✓')

    # ── Create attacker with trained model + scaler ───────────
    attacker = FGSMAttacker(
        model  = trainer.model,
        scaler = trainer.scaler
    )

    # ── Output paths for this model type ─────────────────────
    output_dir  = os.path.join(RESULTS_DIR, model_type)
    os.makedirs(output_dir, exist_ok=True)

    fgsm_path = os.path.join(
        output_dir, 'attack_results_fgsm.json'
    )
    pgd_path  = os.path.join(
        output_dir, 'attack_results_pgd.json'
    )

    # ── FGSM epsilon sweep ────────────────────────────────────
    print(f'\n  Running FGSM sweep...')
    t_fgsm   = time.time()

    fgsm_results = epsilon_sweep(
        attacker    = attacker,
        X_attack    = X_attack,
        y_attack    = y_attack,
        epsilons    = EPSILONS,
        attack_type = 'fgsm'
    )

    t_fgsm = time.time() - t_fgsm
    save_results(fgsm_results, fgsm_path)
    print(f'  FGSM sweep done in {t_fgsm:.1f}s')

    # ── PGD epsilon sweep ─────────────────────────────────────
    print(f'\n  Running PGD sweep ({N_PGD_STEPS} steps)...')
    t_pgd = time.time()

    pgd_results = epsilon_sweep(
        attacker     = attacker,
        X_attack     = X_attack,
        y_attack     = y_attack,
        epsilons     = EPSILONS,
        attack_type  = 'pgd',
        n_pgd_steps  = N_PGD_STEPS
    )

    t_pgd = time.time() - t_pgd
    save_results(pgd_results, pgd_path)
    print(f'  PGD sweep done in {t_pgd:.1f}s')

    return {
        'fgsm': fgsm_results,
        'pgd' : pgd_results
    }


# ─────────────────────────────────────────────────────────────
#  STEP 6 — COMPARE AND SAVE SUMMARY
# ─────────────────────────────────────────────────────────────
def compare_and_save(std_metrics : dict,
                     adv_metrics : dict,
                     std_attacks : dict,
                     adv_attacks : dict):
    """
    Compare standard vs adversarial model results.
    Compute defence gains. Save summary report.

    Defence gain at epsilon ε:
      gain = std_evasion_rate(ε) - adv_evasion_rate(ε)
      Positive = adversarial model better (resists attack more)
      All gains should be positive — if negative → training bug

    Args:
        std_metrics: metrics dict from standard model training
        adv_metrics: metrics dict from adversarial model training
        std_attacks: attack results dict for standard model
        adv_attacks: attack results dict for adversarial model
    """
    print(f'\n{"=" * 60}')
    print(f'  STEP 6 — Results Summary')
    print(f'{"=" * 60}')

    # ── Model accuracy comparison ─────────────────────────────
    std_acc = std_metrics['accuracy'] * 100
    adv_acc = adv_metrics['accuracy'] * 100
    tradeoff = std_acc - adv_acc

    print(f'\n  Model Accuracy:')
    print(f'  {"─" * 40}')
    print(f'  Standard model   : {std_acc:.2f}%')
    print(f'  Adversarial model: {adv_acc:.2f}%')
    print(f'  Accuracy trade-off: {tradeoff:.2f}%')
    print(f'  (small trade-off for better robustness)')

    # ── Defence gain table ────────────────────────────────────
    print(f'\n  Defence Gain — FGSM Attack:')
    print(f'  {"─" * 55}')
    print(f'  {"ε":<8} {"Std Evasion":>12} '
          f'{"Adv Evasion":>12} {"Defence Gain":>14}')
    print(f'  {"─" * 55}')

    fgsm_gains = []
    for i, eps in enumerate(EPSILONS):
        std_ev  = std_attacks['fgsm']['evasion_rates'][i]
        adv_ev  = adv_attacks['fgsm']['evasion_rates'][i]
        gain    = std_ev - adv_ev
        fgsm_gains.append(gain)

        # Mark positive gains with ✓ (defence working)
        marker = '✓' if gain > 0 else '✗'
        print(f'  {eps:<8} {std_ev:>11.1f}% '
              f'{adv_ev:>11.1f}% '
              f'{gain:>+13.1f}% {marker}')

    print(f'\n  Defence Gain — PGD Attack:')
    print(f'  {"─" * 55}')
    print(f'  {"ε":<8} {"Std Evasion":>12} '
          f'{"Adv Evasion":>12} {"Defence Gain":>14}')
    print(f'  {"─" * 55}')

    pgd_gains = []
    for i, eps in enumerate(EPSILONS):
        std_ev = std_attacks['pgd']['evasion_rates'][i]
        adv_ev = adv_attacks['pgd']['evasion_rates'][i]
        gain   = std_ev - adv_ev
        pgd_gains.append(gain)

        marker = '✓' if gain > 0 else '✗'
        print(f'  {eps:<8} {std_ev:>11.1f}% '
              f'{adv_ev:>11.1f}% '
              f'{gain:>+13.1f}% {marker}')

    # ── Build summary report ──────────────────────────────────
    summary = {

        'dataset': 'CICIDS2017',

        'models': {
            'standard': {
                'accuracy'  : round(std_acc,   2),
                'input_dim' : std_metrics['input_dim'],
                'epochs'    : std_metrics['epochs'],
                'type'      : 'standard'
            },
            'adversarial': {
                'accuracy'      : round(adv_acc,  2),
                'input_dim'     : adv_metrics['input_dim'],
                'epochs'        : adv_metrics['epochs'],
                'type'          : 'adversarial',
                'fgsm_epsilon'  : FGSM_EPSILON
            }
        },

        'accuracy_tradeoff': round(tradeoff, 2),

        'attack_config': {
            'epsilons'      : EPSILONS,
            'attack_samples': ATTACK_SAMPLES,
            'n_pgd_steps'   : N_PGD_STEPS
        },

        'fgsm_results': {
            'standard'  : std_attacks['fgsm']['evasion_rates'],
            'adversarial': adv_attacks['fgsm']['evasion_rates'],
            'defence_gains': [
                round(g, 2) for g in fgsm_gains
            ]
        },

        'pgd_results': {
            'standard'   : std_attacks['pgd']['evasion_rates'],
            'adversarial': adv_attacks['pgd']['evasion_rates'],
            'defence_gains': [
                round(g, 2) for g in pgd_gains
            ]
        },

        'summary': {
            'max_fgsm_evasion_std' : max(
                std_attacks['fgsm']['evasion_rates']
            ),
            'max_fgsm_evasion_adv' : max(
                adv_attacks['fgsm']['evasion_rates']
            ),
            'max_pgd_evasion_std'  : max(
                std_attacks['pgd']['evasion_rates']
            ),
            'max_pgd_evasion_adv'  : max(
                adv_attacks['pgd']['evasion_rates']
            ),
            'max_fgsm_defence_gain': max(fgsm_gains),
            'max_pgd_defence_gain' : max(pgd_gains),
            'pgd_stronger_than_fgsm': (
                max(std_attacks['pgd']['evasion_rates']) >
                max(std_attacks['fgsm']['evasion_rates'])
            )
        }
    }

    # ── Save summary JSON ─────────────────────────────────────
    summary_path = os.path.join(RESULTS_DIR, 'summary_report.json')
    os.makedirs(RESULTS_DIR, exist_ok=True)

    with open(summary_path, 'w') as f:
        json.dump(summary, f, indent=2)

    print(f'\n  {"─" * 40}')
    print(f'  Summary saved → {summary_path}')

    # ── Key findings ──────────────────────────────────────────
    print(f'\n  Key Findings:')
    print(f'  {"─" * 40}')
    print(f'  Max FGSM evasion (std) : '
          f'{summary["summary"]["max_fgsm_evasion_std"]:.1f}%')
    print(f'  Max FGSM evasion (adv) : '
          f'{summary["summary"]["max_fgsm_evasion_adv"]:.1f}%')
    print(f'  Max PGD  evasion (std) : '
          f'{summary["summary"]["max_pgd_evasion_std"]:.1f}%')
    print(f'  Max PGD  evasion (adv) : '
          f'{summary["summary"]["max_pgd_evasion_adv"]:.1f}%')
    print(f'  Max FGSM defence gain  : '
          f'{summary["summary"]["max_fgsm_defence_gain"]:.1f}%')
    print(f'  Max PGD  defence gain  : '
          f'{summary["summary"]["max_pgd_defence_gain"]:.1f}%')
    print(f'  PGD stronger than FGSM: '
          f'{summary["summary"]["pgd_stronger_than_fgsm"]}')


# ─────────────────────────────────────────────────────────────
#  MAIN — Full Experiment Pipeline
# ─────────────────────────────────────────────────────────────
def main():
    """
    Run the complete adversarial IDS experiment pipeline.

    Steps:
      1. Load CICIDS2017 dataset (hard stop if missing)
      2. Train standard model
      3. Train adversarial model
      4. Attack standard model (FGSM + PGD)
      5. Attack adversarial model (FGSM + PGD)
      6. Compare results and save summary

    Total expected time: 30-50 minutes on CPU
    """

    # ── Start timer ───────────────────────────────────────────
    t_total = time.time()

    # ── Print experiment banner ───────────────────────────────
    print_banner()

    # ── STEP 1: Load dataset ──────────────────────────────────
    df = load_dataset()

    # ── STEP 2: Train standard model ──────────────────────────
    std_trainer = train_model(df, adversarial=False)
    std_metrics = {
        'accuracy' : std_trainer.history['val_acc'][-1],
        'input_dim': std_trainer.input_dim,
        'epochs'   : EPOCHS,
        'type'     : 'standard'
    }

    # ── STEP 3: Train adversarial model ───────────────────────
    adv_trainer = train_model(df, adversarial=True)
    adv_metrics = {
        'accuracy' : adv_trainer.history['val_acc'][-1],
        'input_dim': adv_trainer.input_dim,
        'epochs'   : EPOCHS,
        'type'     : 'adversarial'
    }

    # ── STEP 4: Attack standard model ─────────────────────────
    std_attacks = run_attacks(
        trainer    = std_trainer,
        df         = df,
        model_type = 'standard'
    )

    # ── STEP 5: Attack adversarial model ──────────────────────
    adv_attacks = run_attacks(
        trainer    = adv_trainer,
        df         = df,
        model_type = 'adversarial'
    )

    # ── STEP 6: Compare and save summary ──────────────────────
    compare_and_save(
        std_metrics = std_metrics,
        adv_metrics = adv_metrics,
        std_attacks = std_attacks,
        adv_attacks = adv_attacks
    )

    # ── Total time ────────────────────────────────────────────
    t_elapsed = time.time() - t_total
    hours     = int(t_elapsed // 3600)
    minutes   = int((t_elapsed % 3600) // 60)
    seconds   = int(t_elapsed % 60)

    print(f'\n{"=" * 60}')
    print(f'  Experiment Complete')
    print(f'{"=" * 60}')
    print(f'  Total time   : {hours:02d}h {minutes:02d}m {seconds:02d}s')
    print(f'  Models saved : {MODELS_DIR}/')
    print(f'  Results saved: {RESULTS_DIR}/')
