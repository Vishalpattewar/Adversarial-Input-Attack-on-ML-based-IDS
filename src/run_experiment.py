# Number of attack samples per sweep
# WHY 1000: representative but fast on CPU (~5-10 min total)
ATTACK_SAMPLES = 1000

# PGD steps per epsilon
# WHY 10: standard in adversarial ML literature
# More steps = stronger attack but slower computation
N_PGD_STEPS = 10


