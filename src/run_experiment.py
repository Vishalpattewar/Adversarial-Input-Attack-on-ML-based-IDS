# =============================================================
# src/run_experiment.py
# =============================================================
# PURPOSE:
#   Main experiment orchestrator for the CICIDS2017
#   Adversarial Input Attack on ML-based IDS project.
#   Runs the complete pipeline from data loading through
#   model training, adversarial attacks, and result saving.
#
# WHAT THIS FILE DOES (in order):
#   STEP 1 — Load network_flows_cicids.csv
#             Validate file exists and columns match
#             Hard stop if file missing (no synthetic fallback)
#
#   STEP 2 — Train STANDARD IDS model
#             IDSTrainer(adversarial=False)
#             Saves: ids_standard.pth
#                    scaler_standard.pkl
#                    metrics_standard.json
#                    history_standard.json
#
#   STEP 3 — Train ADVERSARIAL IDS model
#             IDSTrainer(adversarial=True, fgsm_epsilon=0.80)
#             Saves: ids_adversarial.pth
#                    scaler_adversarial.pkl
#                    metrics_adversarial.json
#                    history_adversarial.json
#
#   STEP 4 — Attack STANDARD model
#             FGSM sweep → results/cicids/standard/
#                          attack_results_fgsm.json
#             PGD  sweep → results/cicids/standard/
#                          attack_results_pgd.json
#
#   STEP 5 — Attack ADVERSARIAL model
#             FGSM sweep → results/cicids/adversarial/
#                          attack_results_fgsm.json
#             PGD  sweep → results/cicids/adversarial/
#                          attack_results_pgd.json
#
#   STEP 6 — Compare models and save summary
#             results/cicids/summary_report.json
#             Prints defence gain table to terminal
#
# KEY DESIGN DECISIONS:
#   WHY HARD STOP if CSV missing:
#     Original NSL-KDD version had generate_synthetic_data()
#     fallback which silently trained on fake data → wrong results
#     This version stops immediately — forces real data usage
#
#   WHY fgsm_epsilon=0.80 for adversarial training:
#     Early NSL-KDD experiments used 0.15 → negative defence gains
#     (adversarial model WORSE than standard at high epsilon)
#     0.80 matches the tested attack range [0.05 → 2.00]
#     Model trains to resist the full range of attacks
#
#   WHY 1000 attack samples (not all attacks):
#     Full attack set = 16,888 samples × 8 epsilons × 2 attacks
#     × 2 models = expensive computation on CPU
#     1000 stratified = statistically representative
#     ~5-10 minutes attack time vs hours for full set
#
#   WHY epsilon range [0.05 → 2.00]:
#     0.05 = minimal perturbation (near-invisible)
#     2.00 = aggressive perturbation (clearly detectable)
#     8 points gives smooth evasion curve for plotting
#
# HOW TO RUN:
#   cd ~/A-IDS/adversarial_ids_cicids/src
#   python3 run_experiment.py
#   Expected time: 30-50 minutes on CPU
#
# PRE-REQUISITE:
#   python3 prepare_cicids.py must be run first
#   data/network_flows_cicids.csv must exist
#
# NEXT STEP:
#   python3 visualise_results.py
#
# CDAC ITISS — Adversarial Input Attack on ML-based IDS
# =============================================================


