    """
    Generate all 3 visualisation plots.

    1. Load all JSON result files
    2. Create output plots/ directory
    3. Generate evasion_curves.png
    4. Generate defence_gain.png
    5. Generate architecture.png
    6. Print summary of generated files
    """
    print(f'\n{"=" * 60}')
    print(f'  Generating Result Visualisations')
    print(f'  Dataset  : CICIDS2017')
    print(f'  CDAC ITISS')
    print(f'{"=" * 60}')
    print(f'  Results  : {RESULTS_DIR}')
    print(f'  Output   : {PLOTS_DIR}')
    print()

    # ── Create plots output directory ────────────────────────
    os.makedirs(PLOTS_DIR, exist_ok=True)

    # ── Load all result JSON files ────────────────────────────
    print(f'  Loading result files...')
    data = load_results()
    print()

    # ── Generate all 3 plots ──────────────────────────────────
    print(f'  Generating plots...')

