FIG_SIZE_W  = (12, 7)  # wide format for evasion curves
FIG_SIZE_SQ = (10, 7)  # slightly square for bar chart


# ─────────────────────────────────────────────────────────────
#  LOAD RESULTS
# ─────────────────────────────────────────────────────────────
def load_results() -> dict:
    """
    Load all JSON result files from results/cicids/.

    FIX 4: Hard stop with clear message if files missing.
    All 5 files must exist before visualisation can proceed.

    Returns:
        data dict with keys:
          'std_fgsm'  : standard model FGSM results
          'std_pgd'   : standard model PGD results
          'adv_fgsm'  : adversarial model FGSM results
          'adv_pgd'   : adversarial model PGD results
          'summary'   : full summary report

    Raises:
        SystemExit: if any required JSON file is missing
    """
    # Define all required input files
    required_files = {
        'std_fgsm' : os.path.join(
            RESULTS_DIR, 'standard',   'attack_results_fgsm.json'
        ),
        'std_pgd'  : os.path.join(
            RESULTS_DIR, 'standard',   'attack_results_pgd.json'
        ),
        'adv_fgsm' : os.path.join(
            RESULTS_DIR, 'adversarial','attack_results_fgsm.json'
        ),
        'adv_pgd'  : os.path.join(
            RESULTS_DIR, 'adversarial','attack_results_pgd.json'
        ),
        'summary'  : os.path.join(
            RESULTS_DIR, 'summary_report.json'
        ),
    }

    # FIX 4: Check all files exist before loading
    missing = [
        path for path in required_files.values()
        if not os.path.exists(path)
    ]
    if missing:
        print(f'\n  ✗ ERROR: Required result files not found:')
        for path in missing:
            print(f'    → {path}')
        print(f'\n  Run run_experiment.py first:')
        print(f'    cd ~/A-IDS/adversarial_ids_cicids/src')
        print(f'    python3 run_experiment.py')
        print()
        sys.exit(1)

    # Load all JSON files
    data = {}
    for key, path in required_files.items():
        with open(path) as f:
            data[key] = json.load(f)
        print(f'  Loaded: {os.path.basename(path)}')

    return data


# ─────────────────────────────────────────────────────────────
#  PLOT 1 — EVASION CURVES
# ─────────────────────────────────────────────────────────────
def plot_evasion_curves(data: dict):
    """
    Plot evasion rate vs epsilon for all 4 attack combinations.

    4 curves:
      Standard    + FGSM  (red   solid)
      Standard    + PGD   (red   dashed)
      Adversarial + FGSM  (cyan  solid)
      Adversarial + PGD   (cyan  dashed)

    Lower curves = more robust model
    Adversarial model curves should be BELOW standard curves
    showing that adversarial training reduces evasion rate

    FIX 2: Y-axis formatter uses y directly (already in %)
            NOT y*100 which would give 8500% for 85% values

    Args:
        data: dict from load_results()

    Saves:
        results/cicids/plots/evasion_curves.png
    """
    # Extract data
    epsilons     = data['std_fgsm']['epsilons']
    std_fgsm_ev  = data['std_fgsm']['evasion_rates']
    std_pgd_ev   = data['std_pgd']['evasion_rates']
    adv_fgsm_ev  = data['adv_fgsm']['evasion_rates']
    adv_pgd_ev   = data['adv_pgd']['evasion_rates']

    # ── Create figure ─────────────────────────────────────────
    plt.style.use(STYLE)
    fig, ax = plt.subplots(figsize=FIG_SIZE_W, dpi=FIG_DPI)

    # ── Plot 4 evasion curves ─────────────────────────────────
    # Standard model — red (vulnerable, higher evasion expected)
    ax.plot(
        epsilons, std_fgsm_ev,
        color     = '#FF4444',
        linewidth = 2.5,
        linestyle = '-',
        marker    = 'o',
        markersize= 7,
        label     = 'Standard Model + FGSM'
    )
    ax.plot(
        epsilons, std_pgd_ev,
        color     = '#FF8888',
        linewidth = 2.5,
        linestyle = '--',
        marker    = 's',
        markersize= 7,
        label     = 'Standard Model + PGD'
    )

    # Adversarial model — cyan (hardened, lower evasion expected)
    ax.plot(
        epsilons, adv_fgsm_ev,
        color     = '#00CCFF',
        linewidth = 2.5,
        linestyle = '-',
        marker    = 'o',
        markersize= 7,
        label     = 'Adversarial Model + FGSM'
    )
    ax.plot(
        epsilons, adv_pgd_ev,
        color     = '#88DDFF',
        linewidth = 2.5,
        linestyle = '--',
        marker    = 's',
        markersize= 7,
        label     = 'Adversarial Model + PGD'
    )

    # ── Shade defence region ──────────────────────────────────
    # Fill between standard and adversarial FGSM curves
    # Green shading = area where adversarial training helps
    ax.fill_between(
        epsilons,
        std_fgsm_ev,
        adv_fgsm_ev,
        alpha     = 0.15,
        color     = '#00FF00',
        label     = 'Defence Region (FGSM)'
    )

    # ── Axes labels and title ─────────────────────────────────
    ax.set_xlabel(
        'Perturbation Budget (ε)',
        fontsize   = 13,
        labelpad   = 10
    )
    ax.set_ylabel(
        'Evasion Rate (%)',
        fontsize   = 13,
        labelpad   = 10
    )
    ax.set_title(
        'CICIDS2017 — Adversarial Attack Evasion Curves\n'
        'FGSM and PGD Attacks: Standard vs Adversarially-Hardened IDS',
        fontsize   = 14,
        fontweight = 'bold',
        pad        = 15
    )

    # ── X-axis ticks — one per epsilon value ──────────────────
    ax.set_xticks(epsilons)
    ax.set_xticklabels(
        [str(e) for e in epsilons],
        fontsize = 10
    )

    # ── Y-axis formatter ──────────────────────────────────────
    # FIX 2: Use y directly — data is already in %
    # Original bug: f'{y*100:.0f}%' → showed 8500% for 85%
    ax.yaxis.set_major_formatter(
        mticker.FuncFormatter(lambda y, _: f'{y:.1f}%')
    )
    ax.set_ylim(bottom=0)
    ax.tick_params(axis='y', labelsize=10)

    # ── Grid ──────────────────────────────────────────────────
    ax.grid(
        True,
        alpha     = 0.2,
        linestyle = '--',
        linewidth = 0.8
    )

    # ── Legend ────────────────────────────────────────────────
    ax.legend(
        loc       = 'upper left',
        fontsize  = 10,
        framealpha= 0.3,
        edgecolor = 'white'
    )

    # ── Annotation — max evasion point ────────────────────────
    max_std_ev  = max(std_fgsm_ev)
    max_eps_idx = std_fgsm_ev.index(max_std_ev)
    max_eps     = epsilons[max_eps_idx]

    ax.annotate(
        f'Max evasion\n{max_std_ev:.1f}% at ε={max_eps}',
        xy         = (max_eps, max_std_ev),
        xytext     = (max_eps - 0.3, max_std_ev - 10),
        fontsize   = 9,
        color      = '#FF4444',
        arrowprops = dict(
            arrowstyle = '->',
            color      = '#FF4444',
            lw         = 1.5
        )
    )

    # ── Watermark ─────────────────────────────────────────────
    fig.text(
        0.99, 0.01,
        'CDAC ITISS — Adversarial IDS on CICIDS2017',
        ha        = 'right',
        va        = 'bottom',
        fontsize  = 8,
        color     = 'gray',
        alpha     = 0.5
    )

    # ── Save plot ─────────────────────────────────────────────
    output_path = os.path.join(PLOTS_DIR, 'evasion_curves.png')
    plt.tight_layout()
    plt.savefig(output_path, dpi=FIG_DPI, bbox_inches='tight')
    plt.close()

    print(f'  ✓ Saved: {output_path}')


# ─────────────────────────────────────────────────────────────
#  PLOT 2 — DEFENCE GAIN
# ─────────────────────────────────────────────────────────────
def plot_defence_gain(data: dict):
    """
    Plot defence gain of adversarial training at each epsilon.

    Defence gain = std_evasion_rate - adv_evasion_rate
    Positive = adversarial model is better (resists more)
    Negative = adversarial model is worse (training issue)

    2 bar groups per epsilon:
      Green bars  = FGSM defence gain
      Orange bars = PGD defence gain

    All bars should be positive for a well-trained model.
    Taller bars = stronger defence at that epsilon value.

    FIX 2: Y-axis formatter uses y directly (already in %)

    Args:
        data: dict from load_results()

    Saves:
        results/cicids/plots/defence_gain.png
    """
    # Extract data from summary
    summary    = data['summary']
    epsilons   = summary['attack_config']['epsilons']
    fgsm_gains = summary['fgsm_results']['defence_gains']
    pgd_gains  = summary['pgd_results']['defence_gains']

    # ── Create figure ─────────────────────────────────────────
    plt.style.use(STYLE)
    fig, ax = plt.subplots(figsize=FIG_SIZE_SQ, dpi=FIG_DPI)

    # ── Bar chart setup ───────────────────────────────────────
    x         = np.arange(len(epsilons))
    bar_width = 0.35    # width of each bar group

    # ── Plot FGSM defence gains ───────────────────────────────
    bars_fgsm = ax.bar(
        x - bar_width / 2,
        fgsm_gains,
        width     = bar_width,
        label     = 'FGSM Defence Gain',
        color     = '#00CC66',
        alpha     = 0.85,
        edgecolor = 'white',
        linewidth = 0.5
    )

    # ── Plot PGD defence gains ────────────────────────────────
    bars_pgd = ax.bar(
        x + bar_width / 2,
        pgd_gains,
        width     = bar_width,
        label     = 'PGD Defence Gain',
        color     = '#FF8800',
        alpha     = 0.85,
        edgecolor = 'white',
        linewidth = 0.5
    )

    # ── Value labels on each bar ──────────────────────────────
    # Show gain value above/below each bar for readability
    for bar in bars_fgsm:
        height = bar.get_height()
        va     = 'bottom' if height >= 0 else 'top'
        offset = 0.5 if height >= 0 else -0.5
        ax.text(
            bar.get_x() + bar.get_width() / 2,
            height + offset,
            f'{height:.1f}%',
            ha       = 'center',
            va       = va,
            fontsize = 8,
            color    = '#00CC66'
        )

    for bar in bars_pgd:
        height = bar.get_height()
        va     = 'bottom' if height >= 0 else 'top'
        offset = 0.5 if height >= 0 else -0.5
        ax.text(
            bar.get_x() + bar.get_width() / 2,
            height + offset,
            f'{height:.1f}%',
            ha       = 'center',
            va       = va,
            fontsize = 8,
            color    = '#FF8800'
        )

    # ── Zero reference line ───────────────────────────────────
    # Shows where gains cross from positive to negative
    ax.axhline(
        y         = 0,
        color     = 'white',
        linewidth = 1.0,
        alpha     = 0.5,
        linestyle = '-'
    )

    # ── Axes labels and title ─────────────────────────────────
    ax.set_xlabel(
        'Perturbation Budget (ε)',
        fontsize  = 13,
        labelpad  = 10
    )
    ax.set_ylabel(
        'Defence Gain (%)',
        fontsize  = 13,
        labelpad  = 10
    )
    ax.set_title(
        'CICIDS2017 — Adversarial Training Defence Gain\n'
        'Reduction in Evasion Rate: Standard vs Adversarial Model',
        fontsize   = 14,
        fontweight = 'bold',
        pad        = 15
    )

    # ── X-axis ticks ──────────────────────────────────────────
    ax.set_xticks(x)
    ax.set_xticklabels(
        [f'ε={e}' for e in epsilons],
        fontsize = 10,
        rotation = 15
    )

    # ── Y-axis formatter ──────────────────────────────────────
    # FIX 2: y is already in % — do NOT multiply by 100
    ax.yaxis.set_major_formatter(
        mticker.FuncFormatter(lambda y, _: f'{y:.1f}%')
    )
    ax.tick_params(axis='y', labelsize=10)

    # ── Grid ──────────────────────────────────────────────────
    ax.grid(
        True,
        axis      = 'y',
        alpha     = 0.2,
        linestyle = '--',
        linewidth = 0.8
    )

    # ── Legend ────────────────────────────────────────────────
    ax.legend(
        fontsize  = 11,
        framealpha= 0.3,
        edgecolor = 'white',
        loc       = 'upper left'
    )

    # ── Annotation — positive region label ───────────────────
    ax.text(
        0.98, 0.95,
        '↑ Positive = Adversarial training effective',
        transform  = ax.transAxes,
        ha         = 'right',
        va         = 'top',
        fontsize   = 9,
        color      = '#00CC66',
        alpha      = 0.8
    )

    # ── Watermark ─────────────────────────────────────────────
    fig.text(
        0.99, 0.01,
        'CDAC ITISS — Adversarial IDS on CICIDS2017',
        ha        = 'right',
        va        = 'bottom',
        fontsize  = 8,
        color     = 'gray',
        alpha     = 0.5
    )

    # ── Save plot ─────────────────────────────────────────────
    output_path = os.path.join(PLOTS_DIR, 'defence_gain.png')
    plt.tight_layout()
    plt.savefig(output_path, dpi=FIG_DPI, bbox_inches='tight')
    plt.close()

    print(f'  ✓ Saved: {output_path}')


# ─────────────────────────────────────────────────────────────
#  PLOT 3 — ARCHITECTURE DIAGRAM
# ─────────────────────────────────────────────────────────────
def plot_architecture():
    """
    Generate IDSNet neural network architecture diagram.

    Shows the 4-layer MLP architecture visually:
      Input(52) → BatchNorm → Dense(256) → ReLU → Dropout(0.3)
               → Dense(128) → ReLU → Dropout(0.3)
               → Dense(64)  → ReLU
               → Dense(1)   → Sigmoid → Output

    Used in project report and presentation slides.
    Drawn using matplotlib patches (no external libraries).

    Saves:
        results/cicids/plots/architecture.png
    """
    plt.style.use(STYLE)
    fig, ax = plt.subplots(figsize=(14, 8), dpi=FIG_DPI)
    ax.set_xlim(0, 14)
    ax.set_ylim(0, 8)
    ax.axis('off')

    # ── Layer definitions ─────────────────────────────────────
    # Each layer: (x_center, label, sublabel, color)
    layers = [
        (1.2,  'INPUT',      '52 features',      '#4444FF'),
        (3.0,  'BatchNorm',  'Normalise',         '#888888'),
        (5.0,  'Dense',      '256 neurons\nReLU + Drop(0.3)', '#FF8800'),
        (7.5,  'Dense',      '128 neurons\nReLU + Drop(0.3)', '#FF8800'),
        (10.0, 'Dense',      '64 neurons\nReLU',  '#FF8800'),
        (12.0, 'Dense',      '1 neuron\nSigmoid', '#FF4444'),
        (13.5, 'OUTPUT',     '0=Normal\n1=Attack','#00CC66'),
    ]

    box_w = 1.2   # box width
    box_h = 2.0   # box height
    y_ctr = 4.0   # vertical center

    prev_x = None

    for (x, label, sublabel, color) in layers:

        # Draw layer box
        box = mpatches.FancyBboxPatch(
            (x - box_w / 2, y_ctr - box_h / 2),
            box_w, box_h,
            boxstyle  = 'round,pad=0.1',
            facecolor = color,
            edgecolor = 'white',
            linewidth = 1.5,
            alpha     = 0.85
        )
        ax.add_patch(box)

        # Layer name (top)
        ax.text(
            x, y_ctr + 0.5,
            label,
            ha         = 'center',
            va         = 'center',
            fontsize   = 9,
            fontweight = 'bold',
            color      = 'white'
        )

        # Sub-label (bottom)
        ax.text(
            x, y_ctr - 0.45,
            sublabel,
            ha       = 'center',
            va       = 'center',
            fontsize = 7.5,
            color    = 'white',
            alpha    = 0.9
        )

        # Draw arrow from previous layer
        if prev_x is not None:
            ax.annotate(
                '',
                xy     = (x - box_w / 2 - 0.05, y_ctr),
                xytext = (prev_x + box_w / 2 + 0.05, y_ctr),
                arrowprops = dict(
                    arrowstyle = '->',
                    color      = 'white',
                    lw         = 1.8
                )
            )

        prev_x = x

    # ── Title ─────────────────────────────────────────────────
    ax.set_title(
        'IDSNet Architecture — CICIDS2017\n'
        'Binary Classifier: Normal Traffic vs Attack',
        fontsize   = 14,
        fontweight = 'bold',
        color      = 'white',
        pad        = 20
    )

    # ── Architecture summary text ─────────────────────────────
    summary_text = (
        'Input: 52 CICIDS2017 numeric features  |  '
        'StandardScaler + BatchNorm1d normalisation  |  '
        'BCELoss + Adam (lr=1e-3)  |  '
        'StepLR scheduler (γ=0.5, step=10)'
    )
    ax.text(
        7, 0.6,
        summary_text,
        ha        = 'center',
        va        = 'center',
        fontsize  = 8,
        color     = 'lightgray',
        alpha     = 0.8,
        style     = 'italic'
    )

    # ── Parameter count annotation ────────────────────────────
    # 52×256 + 256 = 13,568  (Layer 1)
    # 256×128 + 128 = 32,896 (Layer 2)
    # 128×64 + 64 = 8,256    (Layer 3)
    # 64×1 + 1 = 65          (Output)
    # Total ≈ 54,785 parameters
    total_params = (
        52 * 256 + 256 +
        256 * 128 + 128 +
        128 * 64 + 64 +
        64 * 1 + 1
    )
    ax.text(
        7, 0.15,
        f'Total trainable parameters: {total_params:,}',
        ha        = 'center',
        va        = 'center',
        fontsize  = 9,
        color     = '#00CC66',
        fontweight= 'bold'
    )

    # ── Watermark ─────────────────────────────────────────────
    fig.text(
        0.99, 0.01,
        'CDAC ITISS — Adversarial IDS on CICIDS2017',
        ha        = 'right',
        va        = 'bottom',
        fontsize  = 8,
        color     = 'gray',
        alpha     = 0.5
    )

    # ── Save plot ─────────────────────────────────────────────
    output_path = os.path.join(PLOTS_DIR, 'architecture.png')
    plt.tight_layout()
    plt.savefig(output_path, dpi=FIG_DPI, bbox_inches='tight')
    plt.close()

    print(f'  ✓ Saved: {output_path}')


